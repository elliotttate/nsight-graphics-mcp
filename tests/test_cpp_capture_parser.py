def test_descriptor_heap_timeline_collects_by_heap(tmp_path: Path) -> None:
    proj = tmp_path / "heap_timeline"
    proj.mkdir()
    (proj / "x.cpp").write_text(
        "void play() {\n"
        "    pCommandList->SetDescriptorHeaps(2, g_DescHeaps_0);\n"
        "    pCommandList->SetGraphicsRootDescriptorTable(0, g_GpuDescHandle_3);\n"
        "    pCommandList->SetGraphicsRootDescriptorTable(0, g_GpuDescHandle_4);\n"
        "    pCommandList->SetPipelineState(g_PSO_1);\n"
        "    pCommandList->DrawInstanced(3, 1, 0, 0);\n"
        "}\n",
        encoding="utf-8",
    )
    idx = ccp.index_cpp_project(proj)
    out = ccp.descriptor_heap_timeline(idx.db_path)
    assert out["ok"]
    # Three descriptor-related calls: SetDescriptorHeaps + two SetGraphicsRootDescriptorTable
    fns = [e["function_name"] for e in out["timeline"]]
    assert "SetDescriptorHeaps" in fns
    assert fns.count("SetGraphicsRootDescriptorTable") == 2
    # by_heap_symbol should contain g_DescHeaps_0
    assert "g_DescHeaps_0" in out["by_heap_symbol"]


def test_parse_root_signature_blob_descriptor_table(tmp_path: Path) -> None:
    blob = _build_root_sig_blob_v1(
        [
            {
                "type": "DESCRIPTOR_TABLE",
                "vis": 5,  # PIXEL
                "ranges": [
                    {
                        "range_type": 0,         # SRV
                        "num_descriptors": 4,
                        "base_register": 0,      # t0
                        "register_space": 0,
                        "offset": 0,
                    },
                    {
                        "range_type": 3,         # SAMPLER
                        "num_descriptors": 1,
                        "base_register": 0,      # s0
                        "register_space": 0,
                        "offset": 4,
                    },
                ],
            },
            {
                "type": "32BIT_CONSTANTS",
                "vis": 5,
                "shader_register": 0,
                "register_space": 1,
                "num_32bit_values": 4,
            },
        ]
    )
    out = ccp.parse_root_signature_blob(blob)
    assert out["ok"]
    assert out["header"]["version"] == 1
    assert out["header"]["num_parameters"] == 2
    p0, p1 = out["parameters"]
    assert p0["type"] == "DESCRIPTOR_TABLE"
    assert p0["shader_visibility"] == "PIXEL"
    assert p0["num_ranges"] == 2
    assert p0["ranges"][0]["range_type"] == "SRV"
    assert p0["ranges"][0]["base_shader_register"] == 0
    assert p0["ranges"][1]["range_type"] == "SAMPLER"
    assert p1["type"] == "32BIT_CONSTANTS"
    assert p1["num_32bit_values"] == 4
    assert p1["register_space"] == 1


def test_find_register_for_root_parameter_resolves_t0(tmp_path: Path) -> None:
    blob = _build_root_sig_blob_v1(
        [
            {
                "type": "DESCRIPTOR_TABLE",
                "vis": 5,
                "ranges": [
                    {
                        "range_type": 0,
                        "num_descriptors": 4,
                        "base_register": 0,
                        "register_space": 0,
                        "offset": 0,
                    }
                ],
            },
        ]
    )
    rs = ccp.parse_root_signature_blob(blob)
    hit = ccp.find_register_for_root_parameter(
        rs, register_class="SRV", register=0, space=0
    )
    assert hit is not None
    assert hit["root_parameter_index"] == 0
    assert hit["range_type"] == "SRV"
    assert hit["base_shader_register"] == 0
    assert hit["num_descriptors"] == 4

    # Out-of-range register should not match.
    miss = ccp.find_register_for_root_parameter(
        rs, register_class="SRV", register=99
    )
    assert miss is None


def test_root_signature_summary_finds_blob_from_project(tmp_path: Path) -> None:
    blob = _build_root_sig_blob_v1(
        [
            {
                "type": "DESCRIPTOR_TABLE",
                "vis": 0,
                "ranges": [
                    {
                        "range_type": 0,
                        "num_descriptors": 1,
                        "base_register": 0,
                        "register_space": 0,
                        "offset": 0,
                    }
                ],
            }
        ]
    )
    proj = tmp_path / "rs_project"
    proj.mkdir()
    body = ", ".join(f"0x{b:02X}" for b in blob)
    (proj / "rs.cpp").write_text(
        f"static const unsigned char g_RootSig_blob_0[] = {{ {body} }};\n"
        "void CreateRoot(ID3D12Device* device) {\n"
        "    device->CreateRootSignature(0, g_RootSig_blob_0, sizeof(g_RootSig_blob_0), IID_PPV_ARGS(&g_RS_0));\n"
        "}\n",
        encoding="utf-8",
    )
    idx = ccp.index_cpp_project(proj)
    # The CreateRootSignature heuristic doesn't require pso_resolver to run.
    summary = ccp.root_signature_summary(idx.db_path, proj)
    assert summary["ok"]
    assert summary["blob_count"] >= 1
    rs0 = summary["root_signatures"][0]
    assert rs0["symbol"] == "g_RootSig_blob_0"
    assert rs0["parsed"]["ok"]
    assert rs0["parsed"]["header"]["num_parameters"] == 1


def test_multiline_call_is_folded(tmp_path: Path) -> None:
    """Nsight sometimes splits long calls across lines; the folder should
    join them so the regex still matches."""
    proj = tmp_path / "multiline"
    proj.mkdir()
    (proj / "x.cpp").write_text(
        "void f(VkCommandBuffer cmd) {\n"
        "    vkCmdBindDescriptorSets(cmd,\n"
        "        VK_PIPELINE_BIND_POINT_GRAPHICS,\n"
        "        g_Layout_1,\n"
        "        2,\n"
        "        1,\n"
        "        &g_DescSet_9,\n"
        "        0, nullptr);\n"
        "}\n",
        encoding="utf-8",
    )
    idx = ccp.index_cpp_project(proj)
    rows = ccp.query_calls(idx.db_path, name="vkCmdBindDescriptorSets")
    assert len(rows) == 1
    assert rows[0]["named_args"]["first_set"] == "2"
    assert rows[0]["named_args"]["p_descriptor_sets"] == "&g_DescSet_9"
