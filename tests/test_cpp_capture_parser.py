def test_descriptor_bindings_reconstruction_d3d12(tmp_path: Path) -> None:
    proj = _write_project(tmp_path)
    idx = ccp.index_cpp_project(proj)
    # Get the DrawIndexedInstanced event and reconstruct prior state
    draw = ccp.query_calls(idx.db_path, name="DrawIndexedInstanced")[0]
    state = ccp.descriptor_bindings_for_event(idx.db_path, draw["event_index"])
    d3 = state["d3d12"]
    assert d3["root_signature"]["value"] == "g_RootSig_0"
    assert d3["pipeline_state"]["value"] == "g_PSO_4"
    assert d3["vertex_buffers"]["views"] == "&g_VBV_2"
    assert d3["index_buffer"]["view"] == "&g_IBV_8"
    assert "0" in d3["root_params"]
    assert d3["root_params"]["0"]["call"] == "SetGraphicsRootDescriptorTable"
    assert "1" in d3["root_params"]
    assert d3["root_params"]["1"]["call"] == "SetGraphicsRootConstantBufferView"


