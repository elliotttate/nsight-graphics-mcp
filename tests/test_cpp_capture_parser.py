from nsight_graphics_mcp import cpp_capture_parser as ccp


def _build_root_sig_blob_v1(parameters: list[dict]) -> bytes:
    """Serialise a tiny D3D12 v1.0 root signature blob for testing.

    ``parameters`` items are either::

        {"type": "DESCRIPTOR_TABLE", "vis": 0,
         "ranges": [{"range_type": 0, "num_descriptors": 1,
                     "base_register": 0, "register_space": 0,
                     "offset": 0}, ...]}

        {"type": "32BIT_CONSTANTS", "vis": 5,
         "shader_register": 0, "register_space": 1, "num_32bit_values": 4}

        {"type": "CBV"|"SRV"|"UAV", "vis": 0,
         "shader_register": 2, "register_space": 0}
    """
    import struct

    PARAM_TYPE = {"DESCRIPTOR_TABLE": 0, "32BIT_CONSTANTS": 1, "CBV": 2, "SRV": 3, "UAV": 4}
    # Plan layout: header(24) + params(12 * N) + per-param payloads appended.
    header_size = 24
    param_stride = 12
    params_offset = header_size
    payload_offset = header_size + len(parameters) * param_stride

    out = bytearray()
    out += b"\x00" * payload_offset
    # Header (we'll patch after).
    struct.pack_into(
        "<IIIIII", out, 0,
        1,                          # version
        len(parameters),
        params_offset,
        0,                          # num_static_samplers
        0,                          # static_samplers_offset
        0,                          # flags
    )

    cursor = payload_offset
    for i, p in enumerate(parameters):
        ptype = PARAM_TYPE[p["type"]]
        vis = p["vis"]
        if p["type"] == "DESCRIPTOR_TABLE":
            ranges_offset = cursor + 8
            num_ranges = len(p["ranges"])
            # num_ranges DWORD + ranges_offset DWORD
            out += struct.pack("<II", num_ranges, ranges_offset)
            cursor += 8
            for r in p["ranges"]:
                out += struct.pack(
                    "<IIIII",
                    r["range_type"],
                    r["num_descriptors"],
                    r["base_register"],
                    r["register_space"],
                    r["offset"],
                )
                cursor += 20
            data_field = ranges_offset - 8  # the 8-byte table header offset
            # Restore param record: data_field is the offset to (num_ranges, ranges_offset)
            data_field = ranges_offset - 8
        elif p["type"] == "32BIT_CONSTANTS":
            data_field = cursor
            out += struct.pack(
                "<III", p["shader_register"], p["register_space"], p["num_32bit_values"]
            )
            cursor += 12
        else:  # CBV / SRV / UAV
            data_field = cursor
            out += struct.pack("<II", p["shader_register"], p["register_space"])
            cursor += 8
        struct.pack_into(
            "<III", out, params_offset + i * param_stride,
            ptype, vis, data_field,
        )
    return bytes(out)


D3D12_SAMPLE = textwrap.dedent(
    """\
    // Generated by NVIDIA Nsight Graphics
    void play_frame() {
        pCommandList->SetGraphicsRootSignature(g_RootSig_0);
        pCommandList->SetDescriptorHeaps(2, g_DescHeaps_0);
        pCommandList->SetPipelineState(g_PSO_4);
        pCommandList->SetGraphicsRootDescriptorTable(0, g_GpuDescHandle_3);
        pCommandList->SetGraphicsRootConstantBufferView(1, 0x12345678ULL);
        pCommandList->SetGraphicsRoot32BitConstants(2, 4, &g_Constants_7, 0);
        pCommandList->IASetVertexBuffers(0, 1, &g_VBV_2);
        pCommandList->IASetIndexBuffer(&g_IBV_8);
        pCommandList->OMSetRenderTargets(1, &g_RTV_5, FALSE, &g_DSV_6);
        pCommandList->DrawIndexedInstanced(36, 1, 0, 0, 0);
        // Should be skipped — not a cmd-list method
        pCommandList->AddRef();
    }
    """
)

VK_SAMPLE = textwrap.dedent(
    """\
    void play_frame(VkCommandBuffer cmd) {
        vkCmdBindPipeline(cmd, VK_PIPELINE_BIND_POINT_GRAPHICS, g_Pipeline_0);
        vkCmdBindDescriptorSets(
            cmd, VK_PIPELINE_BIND_POINT_GRAPHICS, g_Layout_1,
            0, 1, &g_DescSet_5,
            0, nullptr);
        vkCmdBindVertexBuffers(cmd, 0, 1, &g_VB_2, &g_VBOffset_2);
        vkCmdBindIndexBuffer(cmd, g_IB_3, 0, VK_INDEX_TYPE_UINT16);
        vkCmdPushConstants(cmd, g_Layout_1, VK_SHADER_STAGE_VERTEX_BIT, 0, 64, &g_PC_data);
        vkCmdDrawIndexed(cmd, 36, 1, 0, 0, 0);
    }
    """
)


def _write_project(tmp_path: Path) -> Path:
    proj = tmp_path / "GeneratedCpp"
    proj.mkdir()
    (proj / "d3d12.cpp").write_text(D3D12_SAMPLE, encoding="utf-8")
    (proj / "vulkan.cpp").write_text(VK_SAMPLE, encoding="utf-8")
    return proj


def test_split_args_handles_nesting_and_strings() -> None:
    raw = '0, &g_X, "a,b,(c)", {1, 2, {3, 4}}, foo<int, float>(1)'
    assert ccp.split_args(raw) == [
        "0", "&g_X", '"a,b,(c)"', "{1, 2, {3, 4}}", "foo<int, float>(1)"
    ]


def test_index_cpp_project_picks_up_both_apis(tmp_path: Path) -> None:
    proj = _write_project(tmp_path)
    idx = ccp.index_cpp_project(proj)
    assert idx.record_count > 0
    assert idx.api_histogram.get("d3d12", 0) >= 9  # 10 cmd-list calls minus AddRef
    assert idx.api_histogram.get("vulkan", 0) == 6
    # The blacklisted AddRef should NOT have been indexed
    rows = ccp.query_calls(idx.db_path, name="AddRef")
    assert rows == []


def test_per_event_named_args_d3d12(tmp_path: Path) -> None:
    proj = _write_project(tmp_path)
    idx = ccp.index_cpp_project(proj)
    # Find the SetGraphicsRootDescriptorTable call and confirm its named args.
    rows = ccp.query_calls(idx.db_path, name="SetGraphicsRootDescriptorTable")
    assert len(rows) == 1
    row = rows[0]
    assert row["named_args"]["root_param_index"] == "0"
    assert row["named_args"]["gpu_descriptor_handle"] == "g_GpuDescHandle_3"

    rows = ccp.query_calls(idx.db_path, name="DrawIndexedInstanced")
    assert len(rows) == 1
    args = rows[0]["named_args"]
    assert args["index_count_per_instance"] == "36"
    assert args["instance_count"] == "1"


